        <br>

        2)     Installation des câbles
        Le câblage consiste à mettre en place un réseau de câbles qui va permettre de relier les serveurs aux
        ordinateurs. Deux options s’offrent aux entreprises :
        -        Il est possible de cacher les câbles dans les murs si le bâtiment est suffisamment récent,
        -        Dans le cas où le bâtiment est ancien, l’entreprise peut opter pour des planchers techniques, des
        faux-plafonds ou des bornes wifi.
        <br><br>
        
        3)     Installation des serveurs
        L’entreprise doit désormais installer des serveurs si elle n’en a pas et s’assurer qu’elle dispose de
        suffisamment de serveurs pour absorber ses besoins. Il est également nécessaire de vérifier la connectique : il
        faut compter un minimum de 2 ports par serveur. Les serveurs peuvent fonctionner sous Windows ou Linux,
        l’entreprise devra donc choisir le système d’exploitation qui lui convient le mieux.
         <br> <br>

        4)     Installation des commutateurs réseaux
        La liaison entre les serveurs et les équipements informatiques d’une entreprise (ordinateurs, imprimantes, etc.)
        se fait par le biais de commutateurs réseaux. Le nombre de commutateurs nécessaires dépendra de votre
        installation, du nombre de serveurs et d’équipements informatiques dans les locaux.
         <br><br>

        5)     Installation et paramétrage des logiciels serveurs
        Une fois toutes les étapes précédentes réalisées, il devient alors nécessaire d’installer les logiciels serveurs
        (Windows ou Linux en fonction de ce qui aura été choisi). Il faudra ensuite attribuer une adresse IP à chaque
        ordinateur ou périphérique du réseau informatique afin de pouvoir tous les identifier.
         <br><br>

        6)     Configuration des droits d’accès
        Afin d’éviter que tout le monde n’ait accès à l’ensemble des données de l’entreprise, il est possible de
        configurer des droits d’accès. On peut ainsi indiquer quels ordinateurs avec quelle adresse IP peut avoir accès
        à un serveur. Ainsi, le service marketing d’une entreprise aura accès à un serveur dédié et le service
        commercial aura accès à un autre serveur qui sera réservé à ce département.
         <br><br>

        7)     Mise en place de leviers de sécurité
        La sécurisation du réseau informatique est critique pour une entreprise, notamment lorsqu’elle a opté pour des
        commutateurs intelligents. Pour cela, elle dispose de plusieurs leviers :
        -        La mise en place d’accès utilisateur sécurisé par un mot de passe,
        -        Le réglage des priorités entre les différents utilisateurs et/ou services
